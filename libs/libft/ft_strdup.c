#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		strlen;
	char	*res;
	int		i;

	strlen = 0;
	while (s1[strlen])
		strlen++;
	i = 0;
	res = (char *)malloc(sizeof(char) * strlen + 1);
	if (res == NULL)
		return (NULL);
	while (s1[i])
	{
		res[i] = s1[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
